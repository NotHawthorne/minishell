#include "libft.h"

int		ft_atoi(const char *str)
{
	int	neg;
	int ret;

	neg = 1;
	ret = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v' ||
			*str == '\f' || *str == '\r' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
		neg = *str++ == '-' ? -1 : 1;
	while (*str >= '0' && *str <= '9')
	{
		if (ret == 214748364 && *str == '8')
			return (-2147483648);
		ret = (ret * 10) + (*str++ - '0');
	}
	return (ret * neg);
}
